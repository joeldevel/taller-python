# Variables
# Las cosas tienen tipo
# No hay que declarar el tipo
# El tipo puede cambiar durante la vida del programa

variable_1 = "moringa"

print(variable_1 ," es ",type(variable_1)) # Nos da la clase str

variable_1 = 123

print(variable_1, " es ",type(variable_1)) # Nos da la clase int

variable_1 = 123.0

print(variable_1, " es ", type(variable_1)) # Nos da la clase float

